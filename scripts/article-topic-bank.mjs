function slugToWords(slug) {
  return slug
    .split("-")
    .map((word) => word.toLowerCase())
    .join(" ");
}

function titleCase(text) {
  const acronyms = new Map([
    ["api", "API"],
    ["cdn", "CDN"],
    ["cpu", "CPU"],
    ["css", "CSS"],
    ["dhcp", "DHCP"],
    ["dns", "DNS"],
    ["gpu", "GPU"],
    ["hdmi", "HDMI"],
    ["html", "HTML"],
    ["https", "HTTPS"],
    ["iaas", "IaaS"],
    ["ip", "IP"],
    ["ipv4", "IPv4"],
    ["ipv6", "IPv6"],
    ["lan", "LAN"],
    ["mac", "MAC"],
    ["nat", "NAT"],
    ["nvme", "NVMe"],
    ["paas", "PaaS"],
    ["psu", "PSU"],
    ["qos", "QoS"],
    ["ram", "RAM"],
    ["saas", "SaaS"],
    ["sd", "SD"],
    ["sdk", "SDK"],
    ["seo", "SEO"],
    ["sim", "SIM"],
    ["sla", "SLA"],
    ["sso", "SSO"],
    ["ssd", "SSD"],
    ["ssid", "SSID"],
    ["tcp", "TCP"],
    ["udp", "UDP"],
    ["uefi", "UEFI"],
    ["ui", "UI"],
    ["url", "URL"],
    ["usb", "USB"],
    ["ux", "UX"],
    ["vpn", "VPN"],
    ["wan", "WAN"],
    ["wi", "Wi"],
    ["fi", "Fi"],
    ["xml", "XML"],
  ]);

  return text
    .split(" ")
    .map((word) => {
      const cleaned = word.toLowerCase();
      if (acronyms.has(cleaned)) {
        return acronyms.get(cleaned);
      }

      return cleaned.charAt(0).toUpperCase() + cleaned.slice(1);
    })
    .join(" ")
    .replace("Wi Fi", "Wi-Fi");
}

function buildTopic(slug, category) {
  const words = slugToWords(slug);
  const label = titleCase(words);
  return {
    slug: `what-is-${slug}`,
    topic: label,
    category,
  };
}

export function getTopicBank() {
  return Object.entries(topicsByCategory)
    .flatMap(([category, slugs]) => slugs.map((slug) => buildTopic(slug, category)))
    .sort((a, b) => a.slug.localeCompare(b.slug));
}
